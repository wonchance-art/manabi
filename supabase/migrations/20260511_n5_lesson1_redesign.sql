**오늘의 패턴**
일본어 첫 문장 틀. "A는 B다"라고 단정해서 말할 때 쓰는 가장 흔한 형태예요.

**구조**
A は B です。
わたしは がくせいです。 (저는 학생입니다)

**🔊 발음 약속 — は는 "와"**
조사로 쓸 때는 「하」가 아니라 「와」. 글로만 は. 일본어 입문 첫 관문이에요.

**확장 — 회화 한 컷을 위해 같이**
의문 ─ A は B ですか。 ("~입니까?")
응답 ─ はい (네) / いいえ (아니요)
부정 ─ A は B じゃないです。 ("~가 아니에요")

**예시 한 컷**
A: あなたは がくせいですか。
B: いいえ、 がくせいじゃないです。せんせいです。

**헷갈리는 점**
「じゃないです」(회화) vs 「じゃありません」(격식). 입문에선 회화체 「じゃないです」로 시작해요.

**오늘의 미션**
배운 5단어로 자기소개 3문장 만들기:
1. わたしは ___ です。
2. あなたは ___ ですか。
3. ___ じゃないです、 ___ です。
