**오늘의 패턴 — A は B です**
일본어 첫 문장 틀. "A는 B다"라고 단정해서 말할 때 쓰는 가장 흔한 형태예요.

わたしは がくせいです。
あなたは せんせいです。
わたしは かんこくじんです。
たなかさんは にほんじんです。

**🔊 발음 약속 — は는 "와"**
조사로 쓸 때만 「와」로 읽어요. 글로는 は. 일본어 입문 첫 관문이라 처음엔 어색해도 곧 자연스러워집니다.

**의문 — ~ですか**
문장 끝에 「か」 하나 붙이면 끝. 한국어처럼 어순도 그대로예요.

あなたは がくせいですか。
はい、 がくせいです。
いいえ、 せんせいです。

**부정 — ~じゃないです**
"~가 아니에요". 회화에서 가장 많이 쓰는 형태입니다.

わたしは にほんじん じゃないです。
かんこくじんです。

**헷갈리는 점**
「じゃないです」(회화체) vs 「じゃありません」(격식). 의미는 같지만 분위기가 달라요. 입문에선 회화체부터 익히는 게 자연스럽습니다.

**오늘의 미션**
