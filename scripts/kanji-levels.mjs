export const LEVEL_ORDER = ['N5', 'N4', 'N3', 'N2', 'N1'];

// 문자 → 급 맵.
export const KANJI_TO_LEVEL = (() => {
  const m = new Map();
  for (const lv of LEVEL_ORDER)
    for (const ch of KANJI_LEVEL_STRINGS[lv]) if (!m.has(ch)) m.set(ch, lv);
  return m;
})();

/**
 * 챕터 레벨(N5/N4/…, OT 포함)에서 "허용되는 한자 급 집합"을 누적으로 만든다.
 * 예: N4 챕터 → N5+N4. OT는 문자 트랙이라 한자 최소여야 하므로 N5 세트 기준.
 * 알 수 없는(리스트에 없는) 챕터 레벨은 N5로 보수 처리.
 */
export function allowedKanjiSet(chapterLevel) {
  const norm = String(chapterLevel || '').toUpperCase();
  const capIdx = norm === 'OT' ? 0 : Math.max(0, LEVEL_ORDER.indexOf(norm));
  const set = new Set();
  for (let i = 0; i <= capIdx; i++)
    for (const ch of KANJI_LEVEL_STRINGS[LEVEL_ORDER[i]]) set.add(ch);
  return set;
}

