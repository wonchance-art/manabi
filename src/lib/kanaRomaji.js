const VOWELS = new Set(['a', 'i', 'u', 'e', 'o']);

export function toRomaji(kana) {
  if (!kana) return '';
  let out = '';
  let i = 0;
  while (i < kana.length) {
    const two = kana.slice(i, i + 2);
    if (COMBO[two]) { out += COMBO[two]; i += 2; continue; }

    const ch = kana[i];
    // 촉음 (っ/ッ): 다음 자음을 두 번
    if (ch === 'っ' || ch === 'ッ') {
      const nextTwo = kana.slice(i + 1, i + 3);
      const nextOne = kana[i + 1];
      const nextRoman = COMBO[nextTwo] || SINGLE[nextOne];
      if (nextRoman) {
        out += nextRoman[0];
        i += 1;
        continue;
      }
    }
    // 장음 (ー): 직전 모음 반복
    if (ch === 'ー') {
      const last = out[out.length - 1];
      if (last && VOWELS.has(last)) out += last;
      i += 1;
      continue;
    }
    if (SINGLE[ch]) {
      out += SINGLE[ch];
      i += 1;
      continue;
    }
    // 모르는 글자(한자·구두점)는 건너뜀
    i += 1;
  }
  return out;
}

// 거의 다 카나로만 이루어진 단어인지
export function isMostlyKana(s) {
  if (!s) return false;
  let kana = 0, total = 0;
  for (const ch of s) {
    if (/[぀-ヿ]/.test(ch)) kana += 1;
    if (!/\s/.test(ch)) total += 1;
  }
  return total > 0 && kana / total >= 0.7;
}
